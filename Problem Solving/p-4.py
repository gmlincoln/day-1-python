"""
Write a Python program to input length and 
width of a rectangle and find area of the given rectangle. 
How to calculate area of a rectangle in Python programming.
 Logic to find area of a rectangle whose length 
and width are given in Python programming.

Input
Enter length: 5
Enter width: 10

Output
Area of rectangle = 50 sq. units

"""
length = int(input("Enter length:"))
width = int(input("Enter width:"))

area = str(length * width)


print("Area of rectangle = " + area + " sq. units")
